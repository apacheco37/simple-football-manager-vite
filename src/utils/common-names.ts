import { faker } from "@faker-js/faker";

import { CountryCode } from "./countries";

export const getRandomFirstName = (countryCode: CountryCode) => {
  const countryFirstNames = NAMES[countryCode]?.firstNames;
  if (!countryFirstNames) {
    return faker.person.firstName("male");
    // throw new Error(`Country ${countryCode} not found - cannot generate names`);
  }
  return countryFirstNames[
    Math.floor(Math.random() * countryFirstNames.length)
  ];
};

